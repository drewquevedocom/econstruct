import { promises as fs } from "node:fs";
import path from "node:path";

const rootDir = process.cwd();
const blogDir = path.join(rootDir, "content", "blog");
const outputPath = path.join(rootDir, "src", "lib", "blog", "content-manifest.ts");

const entries = (await fs.readdir(blogDir))
  .filter((file) => file.endsWith(".md"))
  .sort((a, b) => a.localeCompare(b));

const importLines = entries
  .map((file, index) => `import post${index} from "../../../content/blog/${file}?raw";`)
  .join("\n");

const postList = entries.map((_, index) => `  post${index},`).join("\n");

const fileContents = `// AUTO-GENERATED by scripts/sync-blog-manifest.mjs
// Do not edit manually.

${importLines}

export const rawBlogPosts = [
${postList}
];
`;

await fs.writeFile(outputPath, fileContents, "utf8");
console.log(`Synced blog manifest with ${entries.length} posts.`);
